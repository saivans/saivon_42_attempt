void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	temp;

	i = size - 1 ;
	j = 0 ;
	while (j < i)
	{
		temp = tab[i];
		tab[i] = tab[j];
		tab[j] = temp ;
		j++;
		i--;
	}
}
