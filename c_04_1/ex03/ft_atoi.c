int	ft_atoi(char *str)
{
	int	a;
	int	i;
	int	s;

	i = 0 ;
	s = 0 ;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\t' || str[i] == '\v')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s++;
		i++;
	}
	a = 0 ;
	while (str[i] >= '0' && str[i] <= '9')
		a = a * 10 + (str[i++] - 48);
	if (s % 2 != 0)
		a = -a ;
	return (a);
}
