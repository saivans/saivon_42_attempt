int	ft_sqrt(int nb)
{
	int	k;

	if (nb == 1)
		return (1);
	else if (nb == 0)
		return (0);
	else if (nb >= 2)
	{
		k = 0 ;
		while (k < nb / 2)
		{
			if (k * k == nb)
				return (k);
			k++;
		}
	}
	return (0);
}
