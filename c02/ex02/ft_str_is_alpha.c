int	ft_str_is_alpha(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 1;
	if (str[i] == '\0')
		return (n);
	while (str[i] != '\0')
	{
		if (!((str[i] >= 'a' && str[i] <= 'z') || 
				(str[i] >= 'A' && str[i] <= 'Z')))
		{
			n = 0;
		}
		i++;
	}
	return (n);
}
