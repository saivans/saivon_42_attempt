int	ft_str_is_printable(char *str)
{
	int	i;
	int	status;

	i = 0 ;
	status = 1 ;
	while (str[i])
	{
		if (!(str[i] >= 32 && str[i] <= 126))
			status = 0 ;
		i++;
	}
	return (status);
}
