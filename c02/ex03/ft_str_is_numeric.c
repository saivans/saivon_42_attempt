int	ft_str_is_numeric(char *str)
{
	int	i ;
	int	n ;

	n = 1 ;
	i = 0 ;
	if (*str == '\0')
		n = 1 ;
	while (*(str + i) != '\0')
	{
		if (!(*(str + i) <= 57 && *(str + i) >= 48))
		{
			n = 0 ;
			break ;
		}
		i++;
	}
	return (n);
}
