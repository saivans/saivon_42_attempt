unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	j;

	i = 0 ;
	j = 0 ;
	if (size == 0)
		return (j);
	while (src[j])
	{
		j++;
	}
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0' ;
	return (j);
}
